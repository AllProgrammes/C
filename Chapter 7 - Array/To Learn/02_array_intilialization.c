#include <stdio.h>
int main()
{
    int a[4] = {2, 3, 6, 5};
    printf("The value of int a[0] is %d\n", a[0]);
    printf("The value of int a[1] is %d\n", a[1]);
    printf("The value of int a[2] is %d\n", a[2]);
    printf("The value of int a[3] is %d\n", a[3]);
    float marks[] = {33.3, 55.6};
    printf("The value of int marks[0] is %.2f\n", marks[0]);
    printf("The value of int marks[1] is %.2f\n", marks[1]);
    printf("The value of int marks[2] is %.2f\n", marks[2]);
    return 0;
}